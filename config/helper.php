  public static function validarCodeAllDBs(int $code,array $dbs): bool
  {
    $result = false;
    foreach ($dbs as $db) {

      $con = new conexion($db);
      $codigoResult = $con->SQND(
        "SELECT * FROM producto WHERE codigo = $code"
      );
      if ($codigoResult['rows'] > 0) {
        $result = true;
      }
    }
    return $result;
  }
